
// 동등연산자(abstract eauility)
console.log(1 == '1');

// 일치연산자(strict equility)
console.log(1 === '1');

console.log('hello' == 'hello');  //true
console.log('hello' === 'hello');  //true

console.log(0 == false);  //true
console.log(0 === false);  //false

console.log(null == undefined);
console.log(null === undefined);